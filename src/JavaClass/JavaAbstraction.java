package JavaClass;

/*
 * Abstraction in Java is the process of hiding certain details and showing only
 * essential information to the user. 
 * 
 * Achieve by the use of Abstract Class or Interface
 */
public abstract class JavaAbstraction {

	/*
	 * Declared abstract method makes the class abstract
	 */
	public abstract void abstractMethod();
	
	public void abstractMethodTwo() {
		System.out.println("Abstract class can have a define method.");
	}
	
	/*
	 * Abstract class cannot be instantiated. Abstract class can only be inherited.
	 */
	
	
	
}
