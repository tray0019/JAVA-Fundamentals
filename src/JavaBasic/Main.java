package JavaBasic;

/*
 * Level: 1
 * Every class name must start with upper case letter
 */
public class Main {

	//This main method is a must for the program to be executed
	public static void main(String[] args) {
		
		//This statement print out Hello World in the console
		System.out.println("Hello World");
		
		//When double quotes are use the value becomes a String
		System.out.print("Print without line break -!");
		System.out.println("'println, print with line break'");
		
		//Added empty println for a line break
		System.out.println();
		
		//Numeric does not need double quote.
		System.out.println(64);	
		System.out.println("Print: "+ 64);
		//It works with other arithmetic operator 
		System.out.println(64 + 6);
	
	}
	
}
